<?php 

class Dog extends Animal
{
    private string $fur;

  public function __construct(string $name,int $legs,string $fur)
   {
    $this->name;
    $this->legs;
    $this->fur;
   }

public function getname() {
    return $this->name;
}
public function getlegs() {
    return $this->legs;
}
public function getfur()
{
    return $this->fur;
}
}

